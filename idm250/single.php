  <h3>YALE LAYOUT REWORK</h3>
            <p>In my Human-Computer Interaction class (INFO-110), we were tasked with finding a specific website that we found to have various pain points which would directly impact the user’s understanding and functional tasks to interact with the site.</p>
            <br>
            <p>This assignment specifically focused on the aspect of catering to and understanding the user’s thoughts, motivations, and interpretations behind their decisions whilst interacting with the site. This understanding drove our decisions behind crucial elements of our new layout.</p>
            <div class="projectwork">
              <img src="imagesscaled/yaleold.png" alt="A screenshot of the current website of the Yale School of Art.">
            </div>
            <p>The site that my team and I had found was the main website for the Yale School of Art. It was clear that many of the aspects of the design and the layout were chosen purely in the pursuit of a poppy visual apperance. We could see that these elements would clearly impact the user experience.</p>
            <div class="projectwork">
              <img src="imagesscaled/yalezoom.png" alt="Closer zoomed image of the Yale Site, showing some of the issues with the design circled by the team.">
            </div>
            <p>Specific issues with the site followed the harsh and unorganized placement of certain text blocks, as well as the general poor organization and use of space. While it succeeded in a unique look, these decisions and features made the site very frustrating to use and navigate through, and our team began to think about areas in the site’s layout that could be reworked.</p>
            <div class="projectwork">
              <img src="imagesscaled/usertest.png" alt="Logo and official deliverable work of the User Testing service.">
            </div>
            <p>While it was evident to our team members that the site’s poor design was going to impact the user’s functions and experience, we needed to get real world feedback. We utilized the service UserTesting to accomplish this. Each member of our three person team employed three users to accomplish tasks that we had designed for them to complete, specifically so that they would come into contact with the pain points we discovered.</p>
            <div class="projectwork">
              <img src="imagesscaled/yalecomments.png" alt="Comments from the various users who had carried out tests.">
            </div>
            <p>After the users had completed their tests, it was clear to see that they all had struggled with the tasks that we had designed. We were now able to directly hone our focus on the problems, as our decisions were backed by data from real users who tested them. </p>
            <div class="projectwork">
              <img src="imagesscaled/yalenew.png" alt="New layout of our design for the Yale art website.">
            </div>
            <p>The layout above demonstrates a combination of our team members’ design prowess, as well as the considerations from the input that we received from each member’s three users that were interviewed. I enjoyed this assignment in particular because I got to work with other designers. Having multiple sources of input was a different experience for me and certainly helped to balance out each other’s weaknesses and strengths.</p>
